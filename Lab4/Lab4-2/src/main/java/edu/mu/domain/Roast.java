package edu.mu.domain;

import java.util.List;

public class Roast {
	private String roastType;
	private List<String> roastings;
	public String getRoastType() {
		return roastType;
	}
	public void setRoastType(String roastType) {
		this.roastType = roastType;
	}
	public List<String> getRoastings() {
		return roastings;
	}
	public void setRoastings(List<String> roastings) {
		this.roastings = roastings;
	}
	
}
